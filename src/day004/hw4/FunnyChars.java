package day004.hw4;

public class FunnyChars {
    public static void main(String[] args) {
        // @ COMMERCIAL AT

        char commercialAt = '@';  //  U+0040	@	40	COMMERCIAL AT
        System.out.println(commercialAt);

        char commercialAtDecimal = 64; // Decimal @
        System.out.println(commercialAtDecimal);

        char commercialAtOctal = '\u0040';   // Octal @
        System.out.println(commercialAtOctal);

        // ✆ TELEPHONE LOCATION SIGN

        char telephoneLocationSign = '✆';  //  U+2706	✆	e2 9c 86	TELEPHONE LOCATION SIGN
        System.out.println(telephoneLocationSign);

        char telephoneLocationSignDecimal = 9990; // Decimal ✆
        System.out.println(telephoneLocationSignDecimal);

        char telephoneLocationSignOctal = '\u2706';   // Octal ✆
        System.out.println(telephoneLocationSignOctal);

        // § SECTION SIGN

        char sectionSign = '§';  //  U+00A7	§	194 167	&#167;	§ 	SECTION SIGN
        System.out.println(sectionSign);

        char sectionSignDecimal = 167; // Decimal §
        System.out.println(sectionSignDecimal);

        char sectionSignDecimalOctal = '\u00A7';   // Octal §
        System.out.println(sectionSignDecimalOctal);

        // © COPYRIGHT SIGN

        char copyrightSign = '©';  //  U+00A9	©	194 169	&#169;	© 	COPYRIGHT SIGN
        System.out.println(copyrightSign);

        char copyrightSignDecimal = 169; // Decimal ©
        System.out.println(copyrightSignDecimal);

        char copyrightSignOctal = '\u00A9';   // Octal ©
        System.out.println(copyrightSignOctal);

        // ™ TRADE MARK SIGN

        char tradeMarkSign = '™';  // U+2122	™	226 132 162	&#8482;	™ 	TRADE MARK SIGN
        System.out.println(tradeMarkSign);

        char tradeMarkSignDecimal = 8482; // Decimal ™
        System.out.println(tradeMarkSignDecimal);

        char tradeMarkSignOctal = '\u2122';   // Octal ™
        System.out.println(tradeMarkSignOctal);

        // ₽ RUBLE SIGN

        char rubleSign = '₽';  // U+20BD	₽	226 130 189	&#8381;	₽ 	RUBLE SIGN
        System.out.println(rubleSign);

        char rubleSignDecimal = 8381; // Decimal ₽
        System.out.println(rubleSignDecimal);

        char rubleSignOctal = '\u20BD';   // Octal ₽
        System.out.println(rubleSignOctal);

        // € EURO SIGN

        char euroSign = '€';  // U+20AC	€	226 130 172	&#8364;	€ 	EURO SIGN
        System.out.println(euroSign);

        char euroSignDecimal = 8364; // Decimal €
        System.out.println(euroSignDecimal);

        char euroSignOctal = '\u20AC';   // Octal €
        System.out.println(euroSignOctal);

        // $ DOLLAR SIGN

        char dollarSign = '$';  // U+0024	$	36	&#36;	$ 	DOLLAR SIGN
        System.out.println(dollarSign);

        char dollarSignDecimal = 36; // Decimal $
        System.out.println(dollarSignDecimal);

        char dollarSignOctal = '\u0024';   // Octal $
        System.out.println(dollarSignOctal);

        // ♍ VIRGO

        char virgo = '♍';  // U+264D	♍	226 153 141	&#9805;	♍ 	VIRGO
        System.out.println(virgo);

        char virgoDecimal = 9805; // Decimal ♍
        System.out.println(virgoDecimal);

        char virgoOctal = '\u264D';   // Octal ♍
        System.out.println(virgoOctal);

        // ♕ WHITE CHESS QUEEN

        char whiteChessQueen = '♕';  // U+2655	♕	226 153 149	&#9813;	♕ 	WHITE CHESS QUEEN
        System.out.println(whiteChessQueen);

        char whiteChessQueenDecimal = 9813; // Decimal ♕
        System.out.println(whiteChessQueenDecimal);

        char whiteChessQueenOctal = '\u2655';   // Octal ♕
        System.out.println(whiteChessQueenOctal);

        // ♥ BLACK HEART SUIT

        char blackHeartSuit = '♥';  // U+2665	♥	226 153 165	&#9829;	♥ 	BLACK HEART SUIT
        System.out.println(blackHeartSuit);

        char blackHeartSuitDecimal = 9829; // Decimal ♥
        System.out.println(blackHeartSuitDecimal);

        char blackHeartSuitOctal = '\u2665';   // Octal ♥
        System.out.println(blackHeartSuitOctal);

        // https://www.utf8-chartable.de/unicode-utf8-table.pl
    }
}
